package com.app.dto;

public class ApiResponse {
	
	
	private int value;
	
	
	public ApiResponse() {
		super();
		
	}


	public ApiResponse(int value) {
		super();
		this.value = value;
	}


	public int getValue() {
		return value;
	}


	public void setValue(int value) {
		this.value = value;
	}
	
	
	
	
}
